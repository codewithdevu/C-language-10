#include <stdio.h>

int main(){
    int i = 10; // declare and initialize 'i' with 10 
    int j = i ;
    int a = 4 , b = 5 , c = 6 , d = 7;   // %d is called format specifier
    // %d is for int , %f for float and %t is for char
    printf("the value of i is %d the value of j is %d\n" , i , j);
    printf("the value of a is %d the value of b is %d\n" , a , b);
    printf("the value of c is %d the value of d is %d\n" , c , d);
    return 0 ;

}